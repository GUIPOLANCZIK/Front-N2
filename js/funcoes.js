function abrirModal() {
    document.getElementById("cortina").style.backgroundColor = "rgba(209,8 , 8,0.3)";
    document.getElementById("modal").style.marginTop = "120px";

}
function fecharModal() {
    document.getElementById("cortina").style.backgroundColor = "rgba(0,0,0,0)";
    document.getElementById("modal").style.marginTop = "-500px";
}